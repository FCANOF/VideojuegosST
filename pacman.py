def square(x, y):
    """Draw square using path at (x, y).

    Args:
        x (int): x coordinate of the ball.
        y (int): y coordinate of the ball.
    """
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

def offset(point):
    """Return offset of point in tiles.

    Args:
        point (_type_): _description_

    Returns:
        _type_: _description_
    """
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

def valid(point):
    """Return True if point is valid in tiles.

    Args:
        point (_type_): _description_

    Returns:
        _type_: _description_
    """
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

def world():
    """Draw world using path.
    """
    bgcolor('DarkOrchid4')    #Color of the field
    path.color('pale green')  #Color of the path

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:                 #Display of the pacman's food
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(5, 'black')

def move():
    """Move pacman and all ghosts.
    """
    writer.undo()
    writer.write(state['score']) #Write the score

    clear()

    if valid(pacman + aim): #Movement of the pacman
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:           #Score increment
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'gold2')

    for point, course in ghosts:    #Ghost movements
        if valid(point + course):
            point.move(course)
        else:                       #The ghost hit a tile
            options = [
                vector(10, 0),
                vector(-10, 0),
                vector(0, 10),
                vector(0, -10),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)

def change(x, y):
    """Change pacman aim if valid.

    Args:
        x (int): x coordinate of the ball.
        y (int): y coordinate of the ball.
    """
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

